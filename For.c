//Tabuado do 5, serve para qualquer tabuada
#include <stdio.h>
#include<stdlib.h>
 
int main () {

   int cont;
	
   for( cont = 1; cont <= 10; cont = cont++ ){
      printf(" 5 X %d = %d\n", cont, 5 * cont);
   }
 
   return 0;
}
































