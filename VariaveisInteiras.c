#include <stdio.h>
#include <stdlib.h>

void main () {
    // Definindo uma variavel
    int a = 5, b;

    // Imprimindo a variavel a
    printf("%d\n", a);

    // Concatenando
    printf("O valor da var a e: %d\n", a);

    // Lendo valores
    scanf("%d", &b);

    // Concatenando
    printf("O valor da var b e: %d\n", b);

    // Mudando o valor de a
    a = 15;
    printf("O novo valor da var a e: %d\n", a);




    // Pausando
    system("pause");


}
